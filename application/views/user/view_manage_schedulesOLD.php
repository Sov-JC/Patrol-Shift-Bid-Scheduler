<h1> My schedules </h1>

<p> The schedules below are schedules you've been assigned to by your supervisors. </p>

<h6>Open schedules</h6>
<p>
    Open schedules are schedules that open for bidding.
    Click on an open schedule to:
    - Make a bid (if you've been notified by email)
    - View bidding progress
</p>

<h6>Closed schedules</h6>
<p>
    Closed schedules are schedules that is no longer accepting bids
    because the bidding process has been complete.
    Click on an open schedule to:
    - View p-sheets
    - View shift-lineup
    - Request days off
    - Sign up for overtime (when available)
</p>
